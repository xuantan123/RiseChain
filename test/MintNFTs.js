const contract = new ethers.Contract(CONTRACT_ADDRESS, abi, signer);
async function main() {
  
  // Mint 1 NFT (tokenId = 1)
  const tokenId = 2;
  const amount = 1;
  const recipient = "0x641DEa2c82c1114E84E28B8B0A7222c5b34E696B"; // Mint cho chính deployer (có thể thay đổi địa chỉ nếu cần)

  console.log(`Minting ${amount} of token ID ${tokenId} to ${recipient}`);
  const mintTx = await contract.mint(recipient, tokenId, amount);
  await mintTx.wait();
  console.log("NFT minted successfully!");

  // Kiểm tra số dư của tokenId 1
  const balance = await contract.balanceOf(recipient, tokenId);
  console.log(`Balance of tokenId ${tokenId} for ${recipient}: ${balance.toString()}`);

  // Sau khi kiểm tra số dư, thêm code này
  const totalSupply = await contract.totalSupply(tokenId);
  console.log(`Total supply của tokenId ${tokenId}: ${totalSupply.toString()}`);
}

// Thực thi script
if (require.main === module) {
  main()
    .then(() => process.exit(0))
    .catch((error) => {
      console.error(error);
      process.exit(1);
    });
}

